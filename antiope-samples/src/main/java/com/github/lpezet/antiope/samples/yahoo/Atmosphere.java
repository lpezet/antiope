package com.github.lpezet.antiope.samples.yahoo;

class Atmosphere {
	
	private int mHumidity;
	private double mVisibility;
	private double mPressure;
	private int mRising;
	
	public int getHumidity() {
		return mHumidity;
	}
	public void setHumidity(int pHumidity) {
		mHumidity = pHumidity;
	}
	public double getVisibility() {
		return mVisibility;
	}
	public void setVisibility(double pVisibility) {
		mVisibility = pVisibility;
	}
	public double getPressure() {
		return mPressure;
	}
	public void setPressure(double pPressure) {
		mPressure = pPressure;
	}
	public int getRising() {
		return mRising;
	}
	public void setRising(int pRising) {
		mRising = pRising;
	}
	
}