package com.github.lpezet.antiope.samples.yahoo;

/**
 * @author luc
 *
 */
public class Units {

	private String mTemperature;
	private String mDistance;
	private String mPressure;
	private String mSpeed;
	public String getTemperature() {
		return mTemperature;
	}
	public void setTemperature(String pTemperature) {
		mTemperature = pTemperature;
	}
	public String getDistance() {
		return mDistance;
	}
	public void setDistance(String pDistance) {
		mDistance = pDistance;
	}
	public String getPressure() {
		return mPressure;
	}
	public void setPressure(String pPressure) {
		mPressure = pPressure;
	}
	public String getSpeed() {
		return mSpeed;
	}
	public void setSpeed(String pSpeed) {
		mSpeed = pSpeed;
	}
	
	
}
