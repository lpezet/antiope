package com.github.lpezet.antiope.samples.yahoo;

class Wind {
	
	private double mChill;
	private double mSpeed;
	private int mDirection;
	public double getChill() {
		return mChill;
	}
	public void setChill(double pChill) {
		mChill = pChill;
	}
	public double getSpeed() {
		return mSpeed;
	}
	public void setSpeed(double pSpeed) {
		mSpeed = pSpeed;
	}
	public int getDirection() {
		return mDirection;
	}
	public void setDirection(int pDirection) {
		mDirection = pDirection;
	}
	
}